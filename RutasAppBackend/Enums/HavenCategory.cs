namespace RutasAppBackend.Enums
{
    /// <summary>
    /// Define las categorías de establecimientos que sirven como refugios seguros.
    /// </summary>
    public enum HavenCategory
    {
        /// <summary>
        /// Farmacia, generalmente disponibles 24h.
        /// </summary>
        Pharmacy,

        /// <summary>
        /// Gasolinera o estación de servicio.
        /// </summary>
        GasStation,

        /// <summary>
        /// Establecimiento de hostelería o restauración.
        /// </summary>
        BarRestaurant,

        /// <summary>
        /// Tienda o comercio local certificado.
        /// </summary>
        LocalShop,

        /// <summary>
        /// Centro oficial o dependencia pública.
        /// </summary>
        PublicBuilding,

        /// <summary>
        /// Otra categoría de refugio.
        /// </summary>
        Other
    }
}
